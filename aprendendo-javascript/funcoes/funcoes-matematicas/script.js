let x = 10.38;

// Arredondar pra cima
console.log(Math.ceil(x));

// Arredondar pra baixo
console.log(Math.floor(x));

// Arredondar conforme o peso da fração(casa decimal <=4, arredonda pra baixo, casa decimal >=5, arredonda pra cima)
console.log(Math.round(5.8));
console.log(Math.round(5.3));

// Fornece um número aletório entre 0 e 1
console.log(Math.random());


